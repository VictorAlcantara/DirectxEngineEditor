#include "../engine/Chronometer.h"

//-------------------------------------------------------------------
//	Cdtor
//-------------------------------------------------------------------
Chronometer::Chronometer()
{
	Reset();
}
//-------------------------------------------------------------------
//	Reset
//-------------------------------------------------------------------
void Chronometer::Reset()
{
	m_time = 0.0f;
}
//-------------------------------------------------------------------
//	Update
//-------------------------------------------------------------------
void Chronometer::Update(float deltaTime)
{
	m_time += deltaTime;
}
//-------------------------------------------------------------------
//	GetTime
//-------------------------------------------------------------------
float Chronometer::GetTime()const
{
	return m_time;
}
