#include "../engine/Random.h"
#include "../engine/Math.h"

//-------------------------------------------------------------------
//	Cdtor
//-------------------------------------------------------------------
Random::Random(uint seed)
{
	m_a = 1664525;
	m_c = 1013904223;
	m_m = (uint)math::pow( 2.0f, 31 );
	SetSeed( seed );
}
//-------------------------------------------------------------------
//	SetSeed
//-------------------------------------------------------------------
void Random::SetSeed(uint seed)
{
	m_seed = seed;
}
//-------------------------------------------------------------------
//	Get
//-------------------------------------------------------------------
float Random::Get()
{
	m_seed = (m_a * m_seed + m_c) % m_m;

	return (float)m_seed / (float)m_m;
}
//-------------------------------------------------------------------
//	GetRange
//-------------------------------------------------------------------
float Random::GetRange(float val1, float val2)
{
	return math::lerp( val1, val2, Get() );
}
//-------------------------------------------------------------------
//	GetSign
//-------------------------------------------------------------------
float Random::GetSign()
{
	return Get() < 0.5f ? -1.0f : 1.0f;
}
